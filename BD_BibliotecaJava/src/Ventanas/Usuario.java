    public void Usuario_load()
    {
        int c;
        try {
            pst = con.prepareStatement("select * from usuario");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                
                Vector v2 = new Vector();
                
                for(int i= 1; i<=c; i++)
                {
                    v2.add(rs.getString("IdUsuario"));
                    v2.add(rs.getString("Nombre"));
                    v2.add(rs.getString("Apellido"));
                    v2.add(rs.getString("Telefono"));
                    v2.add(rs.getString("Nivel"));
                }
                d.addRow(v2);
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

