    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();
        
        int IdUsuario= Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        
        String Nombre = txtNombre.getText();
        String Apellido = txtApellido.getText();
        String Telefono = txtTelefono.getText();
        String Nivel = ComboBoxNivel.getSelectedItem().toString();
     
        try {
            pst = con.prepareStatement("update usuario set Nombre = ?,Apellido = ?,Telefono = ?, Nivel = ? where IdUsuario = ?");
            
            pst.setString(1, Nombre);
            pst.setString(2, Apellido);
            pst.setInt(3, Integer.parseInt(Telefono));
            pst.setString(4, Nivel);
            pst.setInt(5, IdUsuario);
            
         
            int k = pst.executeUpdate();
            
            if (k== 1)
            {
                JOptionPane.showMessageDialog(this, "Usuario Actualizado");
                txtNombre.setText("");
                txtApellido.setText("");                
                txtTelefono.setText("");
                ComboBoxNivel.setSelectedIndex(-1);

                Usuario_load();
                btnAgregar.setEnabled(true);
              
                
                txtNombre.requestFocus();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

