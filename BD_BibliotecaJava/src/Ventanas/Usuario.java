    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
       String Nombre = txtNombre.getText();
       String Apellido = txtApellido.getText();
       String Telefono = txtTelefono.getText();
       String Nivel = ComboBoxNivel.getSelectedItem().toString();
       

       
       
        try {
            pst = con.prepareStatement("insert into usuario(Nombre,Apellido,Telefono,Nivel)values(?,?,?,?)");
            
            pst.setString(1, Nombre);
            pst.setString(2, Apellido);
            pst.setInt(3, Integer.parseInt(Telefono));
            pst.setString(4, Nivel);
            
                
            int x = pst.executeUpdate();
            
            if (x== 1)
            {
                JOptionPane.showMessageDialog(this, "usuario creado");
                txtNombre.setText("");
                txtApellido.setText("");
                txtTelefono.setText("");
                ComboBoxNivel.setSelectedIndex(-1);
                Usuario_load();
              
                
                txtNombre.requestFocus();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error no se a creado el Usuario");
            }
            
        }catch (SQLException ex){
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            
        }          
    }//GEN-LAST:event_btnAgregarActionPerformed

