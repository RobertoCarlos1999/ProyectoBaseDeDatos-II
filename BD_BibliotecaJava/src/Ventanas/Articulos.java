        // TODO add your handling code here:
       String Nombre = jTextField1.getText();
       String Autor = jTextField2.getText();
       String Tipo = jComboBox1.getSelectedItem().toString();
      String NroPaginas = jTextField3.getText();
       String AñoEdicion = jTextField4.getText();

       
       
        try {
            pst = con.prepareStatement("insert into articulos(Nombre,Autor,Tipo,NroPaginas,AñoEdicion)values(?,?,?,?,?)");
            
            pst.setString(1, Nombre);
            pst.setString(2, Autor);
            pst.setString(3, Tipo);
            pst.setInt(4, Integer.parseInt(NroPaginas));
            pst.setString(5, AñoEdicion);
                
            int k = pst.executeUpdate();
            
            if (k== 1)
            {
                JOptionPane.showMessageDialog(this, "articulo creado");
                jTextField1.setText("");
                jTextField2.setText("");
                jComboBox1.setSelectedIndex(-1);
                jTextField3.setText("");
                jTextField4.setText("");
                Articulos_load();
              
                
                jTextField1.requestFocus();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error no se a creado el articulo");
            }
            
        }catch (SQLException ex){
            Logger.getLogger(Articulos.class.getName()).log(Level.SEVERE, null, ex);
            
        }          
