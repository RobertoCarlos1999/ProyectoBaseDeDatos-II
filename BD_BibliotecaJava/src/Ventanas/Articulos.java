    public void Articulos_load()
    {
        int c;
        try {
            pst = con.prepareStatement("select * from articulos");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                
                Vector v2 = new Vector();
                
                for(int i= 1; i<=c; i++)
                {
                    v2.add(rs.getString("IdArticulos"));
                    v2.add(rs.getString("Nombre"));
                    v2.add(rs.getString("Autor"));
                    v2.add(rs.getString("Tipo"));
                    v2.add(rs.getString("NroPaginas"));
                    v2.add(rs.getString("AñoEdicion"));
                }
                d.addRow(v2);
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Articulos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

